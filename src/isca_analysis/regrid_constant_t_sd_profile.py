def main():
    file = Path("/data/scratch/helena.reid/isca_data/papillon_fast/run0059/atmos_averages.nc")
    noise = iris.load_cube(file, "papillon_noise")
    t_pert = iris.load_cube(file, "papillon_t_pert")
    cube = t_pert/noise
    cube.coord('latitude').guess_bounds()
    cube.coord('longitude').guess_bounds()
    grid_areas = iris.analysis.cartography.area_weights(cube)
    new_cube = cube.collapsed(['longitude', 'latitude'], iris.analysis.MEAN, weights=grid_areas)
    print(new_cube)
    profile = np.squeeze(new_cube.data)
    for i, j in enumerate(profile):
        print(f"{0.2*j},&")
    plt.plot(profile)
    plt.plot(T_SD_PAPILLON_ZLEVS)
    plt.show()

if __name__ == "__main__":
    main()
